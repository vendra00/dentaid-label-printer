package com.rockwell.custmes.activities;

public class MethodDescriptorException extends Exception {

    private static final long serialVersionUID = 1L;

    public MethodDescriptorException() {
        super();
    }

    public MethodDescriptorException(String message, Throwable cause) {
        super(message, cause);
    }

    public MethodDescriptorException(String message) {
        super(message);
    }

    public MethodDescriptorException(Throwable cause) {
        super(cause);
    }

}
